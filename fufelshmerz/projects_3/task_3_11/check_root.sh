#!/bin/bash

check_root() {

    if [ $EUID -ne 0 ]; then
        echo "ОШИБКА: Скрипт должен быть запущен от имени суперпользователя!" >&2
        echo "Текущий UID: $EUID" >&2
        echo "Пожалуйста, запустите скрипт от имени root." >&2
        exit 1
    fi

    echo "Проверка пройдена: скрипт запущен от имени root (UID: $EUID)"
    return 0
}

echo "===== Проверка прав доступа ====="

check_root

echo "===== Проверка пройдена ====="
echo "Выполнение скрипта..."

echo "Скрипт завершен."
