#!/bin/bash

echo "Поиск информации о пользователе $USER в файле /etc/passwd"
echo "=================================================="

grep "^$USER:" /etc/passwd

if [ $? -eq 0 ]; then
    echo "=================================================="
    echo "Информация о пользователе $USER найдена."
else
    echo "=================================================="
    echo "Пользователь $USER не найден." >&2
    exit 1
fi
